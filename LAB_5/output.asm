PUBLIC PRINT_BIN
PUBLIC PRINT_HEX
PUBLIC NEW_LINE

PUBLIC QUIT

EXTRN BIN_STR: BYTE
EXTRN HEX_STR: BYTE

DATAS SEGMENT PARA PUBLIC 'DATA'
    BIN_MSG DB "Unsigned binary:", 10, 13, "$"
    HEX_MSG DB "Signed hexadecimal:", 10, 13, "$"
DATAS ENDS

CODES SEGMENT PARA PUBLIC 'CODE'
    ASSUME CS:CODES, DS:DATAS
PRINT_BIN:
    CALL NEW_LINE

    MOV AH, 9
    MOV DX, OFFSET BIN_MSG
    INT 21H

    MOV DX, OFFSET BIN_STR
    INT 21H

    CALL NEW_LINE
    RET

PRINT_HEX:
    CALL NEW_LINE
    
    MOV AH, 9
    MOV DX, OFFSET HEX_MSG
    INT 21H

    MOV DX, OFFSET HEX_STR
    INT 21H

    CALL NEW_LINE
    RET

NEW_LINE:
    MOV AH, 2
    MOV DL, 10
    INT 21H
    MOV DL, 13
    INT 21H
    RET

QUIT:
    MOV AX, 4C00H
    INT 21H
    RET
CODES ENDS
END